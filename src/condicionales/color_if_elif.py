# Este programa identifica un color específico

# Se define la variable color
color = "azul"

# Se comparan diferentes posibles valores del color

if color == "rojo":
    print("El color es rojo.")

elif color == "verde":
    print("El color es verde.")

elif color == "azul":
    print("El color es azul.")

# En este caso no hay else
# Si no coincide con ninguno, simplemente no se ejecuta nada