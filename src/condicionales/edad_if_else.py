# Este programa verifica si una persona puede votar según su edad

# Se define la variable edad
edad = 17

# Se evalúa si la edad es mayor o igual a 18
if edad >= 18:
    # Si la condición es verdadera, se ejecuta este bloque
    print("Puedes votar en las elecciones.")
else:
    # Si la condición es falsa, se ejecuta este bloque alternativo
    print("Aún no tienes edad para votar.")