# Este programa verifica si una persona puede acceder a un crédito

# Se definen las variables
edad = 25
ingresos = 30000

# Se usa el operador AND
# La condición solo será verdadera si ambas condiciones se cumplen
if edad >= 18 and ingresos >= 20000:
    print("Eres elegible para el crédito.")

# Si alguna de las dos condiciones es falsa, no se ejecuta nada