# Este programa verifica si una persona es mayor de edad

# Se define la variable edad con un valor numérico
edad = 20

# Se evalúa la condición: ¿edad es mayor o igual a 18?
if edad >= 18:
    # Si la condición es verdadera (True), se ejecuta este bloque
    print("Eres mayor de edad.")

# Si la condición fuera falsa, no pasaría nada y el programa continúa