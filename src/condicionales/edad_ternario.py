# Este programa determina si una persona es mayor o menor de edad usando un condicional ternario

# Se define la variable edad
edad = 17

# Se utiliza una expresión condicional (ternario)
# Si edad >= 18 → se asigna "Eres mayor de edad."
# Si no → se asigna "Eres menor de edad."
mensaje = "Eres mayor de edad." if edad >= 18 else "Eres menor de edad."

# Se imprime el resultado
print(mensaje)